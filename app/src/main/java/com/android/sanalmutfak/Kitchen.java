class Kitchen {


    public String kitchenname;
    public String kitchenpass;


    public Kitchen(String kitchenname, String kitchenpass) {
        this.kitchenname = kitchenname;

        this.kitchenpass = kitchenpass;

    }


    public String getKitchenname() {
        return kitchenname;
    }

    public String getKitchenpass() {
        return kitchenpass;
    }



}
